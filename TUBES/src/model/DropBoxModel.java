package model;

public class DropBoxModel {
    private Integer idDropbox;
    private String namaDropbox;
    private String alamat;

    public Integer getIdDropbox() {
        return idDropbox;
    }

    public void setIdDropbox(Integer idDropbox) {
        this.idDropbox = idDropbox;
    }

    public String getNamaDropbox() {
        return namaDropbox;
    }

    public void setNamaDropbox(String namaDropbox) {
        this.namaDropbox = namaDropbox;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
}